import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const schemaPath = path.join(rootDir, "shared", "upload-policies.schema.json");
const webOutPath = path.join(rootDir, "web", "src", "app", "shared", "constants", "generated-upload-policies.ts");
const serverOutPath = path.join(rootDir, "server", "src", "modules", "upload", "generated-upload-policies.ts");

const schema = JSON.parse(fs.readFileSync(schemaPath, "utf8"));

function expandValue(value) {
  if (Array.isArray(value)) {
    return value.flatMap((item) => expandValue(item));
  }
  if (typeof value === "string" && value.startsWith("$")) {
    const key = value.slice(1);
    const hit = schema.extensions[key];
    if (!Array.isArray(hit)) {
      throw new Error(`Unknown upload policy extension group: ${value}`);
    }
    return [...hit];
  }
  return [value];
}

function expandTarget(target) {
  return {
    bucket: target.bucket,
    category: target.category,
    visibility: target.visibility,
    accept: expandValue(target.accept).join(","),
    maxSizeBytes: target.maxSizeMb * schema.maxSizeUnit,
    ...(target.allowedMimePrefixes ? { allowedMimePrefixes: expandValue(target.allowedMimePrefixes) } : {}),
    ...(target.allowedMimeTypes ? { allowedMimeTypes: expandValue(target.allowedMimeTypes) } : {}),
    allowedExtensions: expandValue(target.allowedExtensions),
    invalidTypeMessage: target.invalidTypeMessage,
    sizeLimitMessage: target.sizeLimitMessage
  };
}

function stable(value) {
  return JSON.stringify(value, null, 2);
}

function writeIfChanged(filePath, content) {
  const normalized = `${content.trimEnd()}\n`;
  const current = fs.existsSync(filePath) ? fs.readFileSync(filePath, "utf8") : "";
  if (current === normalized) {
    return;
  }
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, normalized, "utf8");
}

const targets = Object.fromEntries(
  Object.entries(schema.targets).map(([key, target]) => [key, expandTarget(target)])
);
const serverPolicies = Object.fromEntries(
  Object.entries(targets).map(([key, target]) => {
    const { bucket: _bucket, category: _category, visibility: _visibility, accept: _accept, ...policy } = target;
    return [key, policy];
  })
);

const header = `// This file is generated by apps/hub-v2/scripts/generate-upload-policies.mjs.\n// Edit apps/hub-v2/shared/upload-policies.schema.json instead.\n`;

writeIfChanged(
  webOutPath,
  `${header}
import type { UploadTargetPolicy } from './upload-policies';

export const UPLOAD_TARGETS = ${stable(targets)} as const satisfies Record<string, UploadTargetPolicy>;

export const INLINE_VIDEO_EXTENSIONS = ${stable(schema.extensions.inlineVideo)} as const;
`
);

writeIfChanged(
  serverOutPath,
  `${header}
import type { UploadPolicy } from './upload-policy';

export const GENERATED_UPLOAD_POLICIES = ${stable(serverPolicies)} as const satisfies Record<string, UploadPolicy>;

export const SERVER_UPLOAD_POLICY_RULES = ${stable(schema.serverPolicyRules)} as const;

export const ALLOWED_UPLOAD_BUCKETS = ${stable(schema.allowedBuckets)} as const;
`
);
